import { createAuthClient } from "better-auth/react";
export const authClient = createAuthClient({
  baseURL:
    process.env.NEXT_PUBLIC_APP_URL ||
    (typeof window !== "undefined" ? window.location.origin : ""),
});
export const {
  signIn,
  signUp,
  useSession,
  signOut,
  resetPassword,
  sendVerificationEmail,
  forgetPassword,
  getSession,
  listSessions,
  revokeSession,
  revokeOtherSessions,
  revokeSessions,
} = authClient;
